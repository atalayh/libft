#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		np;
	long	nb;

	np = 1;
	i = 0;
	nb = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			np = -np;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - 48) * np;
		i++;
		if (nb > 2147483647)
			return (-1);
		else if (nb < -2147483648)
			return (0);
	}
	return ((int)nb);
}
